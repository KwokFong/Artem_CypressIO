//Relational/Comparison Operators
// > - More than
// < - Less than
// >= More than equal
// <= Less than equal

var result = 10 > 5
console.log(result)


//Equality Operators
var x = 1
console.log(x == '1')  // loose comparison: Only checks Value
console.log(x === '1') // Strict comparison: Checks both Value AND Datatype
console.log(x === 1)